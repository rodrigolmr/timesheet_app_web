/// Enum que define as variantes de tema disponíveis no aplicativo.
/// 
/// Cada variante representa um conjunto completo de cores, estilos e dimensões.
enum ThemeVariant {
  /// Tema claro padrão (Azul Corporativo)
  light,
  
  /// Tema escuro (Azul adaptado para modo escuro)
  dark,
  
  /// Tema feminino com cores em tons de rosa
  feminine,
  
  /// Tema verde com cores em tons de verde
  green,
}